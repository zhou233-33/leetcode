func search(nums []int, target int) int {
	if nums[0] > target && nums[len(nums)-1] < target && nums[0] > nums[len(nums)-1] {
		return -1
	}
	if target == nums[0] {
		return 0
	}
	if target == nums[len(nums)-1] {
		return len(nums) - 1
	}

	left := 0
	right := len(nums) - 1
	for left <= right {
		midIndex := (left + right) / 2
		midValue := nums[midIndex]
		if midValue == target {
			return midIndex
		}
		if target > nums[0] {
			if midValue < target {
				if midValue >= nums[0] {
					left = midIndex + 1
					continue
				}
				if midValue <= nums[len(nums)-1] {
					right = midIndex - 1
					continue
				}
			} else {
				right = midIndex - 1
			}
			continue
		}
		if target < nums[len(nums)-1] {
			if midValue < target {
				left = midIndex + 1
			} else {
				if midValue >= nums[0] {
					left = midIndex + 1
					continue
				}
				if midValue <= nums[len(nums)-1] {
					right = midIndex - 1
					continue
				}
			}
			continue
		}
	}
	runtime.BlockProfile()
	return -1
}

